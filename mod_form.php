defined('MOODLE_INTERNAL') || die;

require_once($CFG->dirroot . '/course/moodleform_mod.php');
require_once($CFG->dirroot . '/mod/quizsummary/locallib.php');
require_once($CFG->libdir . '/filelib.php');

class mod_smartlink_mod_form extends moodleform_mod
{
    public function definition()
    {
        $mform = $this->_form;

        $mform->addElement('header', 'general', get_string('general', 'form'));

        $mform->addElement('text', 'name', get_string('name'), array('size' => '48'));
        $mform->setType('name', PARAM_TEXT);
        $mform->addRule('name', null, 'required', null, 'client');
        $mform->addRule('name', get_string('maximumchars', '', 255), 'maxlength', 255, 'client');

        $mform->addElement('text', 'url', get_string('url', 'smartlink'), array('size' => '48'));
        $mform->setType('url', PARAM_TEXT);
        $mform->addRule('url', null, 'required', null, 'client');

        $this->standard_intro_elements(get_string('description', 'core'));

        $this->standard_coursemodule_elements();
        $this->add_action_buttons();
    }

    
}
